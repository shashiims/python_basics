# Write a program to merge two lists
# if list1 [1,2,3] and list2 = [10,20] then answer should be
# [1,2,3,10,20]

list1 = ['G','o','o','d']
list2 = ['E','v','e','n','i','n','g']

outputList = []

for each in list1:
    outputList.append(each)

for each in list2:
    outputList.append(each)

print(outputList)